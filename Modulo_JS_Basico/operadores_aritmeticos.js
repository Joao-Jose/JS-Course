/**
 * Aritméticos
 * + = adição / concatenação
 * - = subtração
 * * = multiplicação
 * / = divisão 
 * ** = potenciação
 * % = resto da divisão
 * -- = incremento
 */



const numero = 10;
const numero1 = 20;
console.log(`soma simples de adição', ${numero + numero1} ` );

// concatenação

const numero3 = '20';
const numero4 = 30 ;
console.log(`concatenação', ${numero3 + numero4 }`);
// união de dois valores

const numero5 = 10;
const numero6 = 20;
console.log(`potenciação", ${numero6 ** numero5}`);


// conversão de strings para inteiros

const number = Number('40')
const numberTwo = 40
console.log(number + numberTwo)

